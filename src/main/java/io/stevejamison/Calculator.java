public class Calculator {

    public int sum(int value1, int value2){

        return value1 + value2;
    }

    public int subtract(int value1, int value2){
        return value1 - value2;
    }

    public int product(int value1, int value2){
        return value1 * value2;
    }

    public int div(int value1, int value2){
        return value1 / value2;
    }
}
